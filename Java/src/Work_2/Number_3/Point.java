package Work_2.Number_3;

public class Point {
    private double X, Y;

    public Point (double x, double y) {
        X = x;
        Y = y;
    }

    public void setXY (double x, double y){
        X = x;
        Y = y;
    }
    public double getX() { return X; }
    public double getY() { return Y; }
}
